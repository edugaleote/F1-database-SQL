CREATE DATABASE F1;
USE F1;

CREATE TABLE DRIVERS (
TEAM VARCHAR (40),	
COUNTRY VARCHAR (40),
NAME VARCHAR (40),
SURNAME VARCHAR (40),
PODIUMS INT,
POINTS FLOAT,
GRANDS_PRIX_ENTERED INT,
WORLD_CHAMPIONSHIPS INT,
HIGHEST_RACE_FINISH INT,
HIGUEST_GRID_POSITION INT,
DATE_BIRTH VARCHAR(15),
PLACE_BIRTH VARCHAR(40));	

CREATE TABLE TEAMS (
NAME VARCHAR(40),
BASE VARCHAR(40),
TEAM_CHIEF VARCHAR(40),
TECHNICAL_CHIEF VARCHAR(40),
CHASSIS VARCHAR(40),
POWER_UNIT VARCHAR(40),
FIRST_ENTRY INT,
WORLD_CHAMPIONSHIPS INT,
HIGHEST_RACE_FINISH INT,
POLE INT,
FASTEST_LAPS INT
);	

CREATE TABLE RACE_RESULTS (
RACEID VARCHAR(40),
POS INT,
NOO INT,
DRIVER_CAR VARCHAR(20),
LAPS INT,
TIME_RETIRES TIME,
PTS INT
);


CREATE TABLE FASTEST_LAPS (
RACEID VARCHAR(40),
POS INT,
NOO INT,
DRIVER_CAR VARCHAR(20),
LAP INT, 
TIME_OF_DAY TIME, 
TIME TIME,
AVG_SPEED FLOAT
);

CREATE TABLE QUALIFYING (
RACE_ID VARCHAR(40),
POS INT,
NOO INT,
DRIVER_CAR VARCHAR(20),
Q1 TIME, 
Q2 TIME,
Q3 TIME,
LAPS INT
);


CREATE TABLE DHL_FAST_LAP_AWARD (
GRAND_PRIX VARCHAR(20),
DRIVER_CAR VARCHAR(20),
TIME float
);


























